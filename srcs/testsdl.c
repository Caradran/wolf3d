void	rescale(t_env *env)
{
	int scale;

	scale = (int)ft_min((int)env->s->w/64, (int)env->s->h/64);
	scale = (int)ft_min((env->s->w - 11 * scale)/64, env->s->h/64);
	scale = (scale < 3) ? 3 : scale;
	env->scale = scale;
	env->refresh = 1;
}

